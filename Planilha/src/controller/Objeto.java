package controller;

public class Objeto {
	
	private String nome;
	private String formula;
	
	public Objeto() {
		this.nome = nome;
		this.formula = formula;
	}
	
	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getFormula() {
		return formula;
	}

	public void setFormula(String formula) {
		this.formula = formula;
	}
}
